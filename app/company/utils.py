def get_officer_choices(page_title):
    """
    法人種別（ページタイトル）に応じて役職の選択肢を返すヘルパー関数。
    """
    if page_title == '株主情報':
        return [
            ('代表取締役', '代表取締役'),
            ('取締役', '取締役'),
            ('会計参与', '会計参与'),
            ('監査役', '監査役'),
            ('その他', 'その他')
        ]
    elif page_title == '社員情報':
        return [
            ('代表社員', '代表社員'),
            ('業務執行社員', '業務執行社員'),
            ('有限責任社員', '有限責任社員'),
            ('無限責任社員', '無限責任社員'),
            ('その他', 'その他')
        ]
    return []
